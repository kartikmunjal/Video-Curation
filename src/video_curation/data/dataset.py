"""
PyTorch Dataset classes for curated and augmented video clips.

Supports reading from a JSONL manifest so downstream training doesn't care
whether clips are real or synthetic.
"""

from __future__ import annotations

import json
import logging
import random
from dataclasses import dataclass
from pathlib import Path
from typing import Optional

import numpy as np
import torch
from torch.utils.data import Dataset

log = logging.getLogger(__name__)

try:
    import decord  # fast video decoder
    from decord import VideoReader, cpu, gpu

    _HAS_DECORD = True
except ImportError:
    _HAS_DECORD = False
    log.warning(
        "decord not installed — falling back to torchvision VideoClips (slower)"
    )


# ── Manifest schema ───────────────────────────────────────────────────────────


@dataclass
class ClipMeta:
    """One row of the JSONL manifest."""

    path: str           # absolute or relative path to video file
    label: str          # action class name
    label_idx: int      # integer class index
    split: str          # train | val | test
    is_synthetic: bool  # True if generated by augmentation pipeline
    duration_sec: float
    fps: float
    width: int
    height: int
    blur_score: float
    motion_score: float
    quality_score: float
    caption: Optional[str] = None
    source_path: Optional[str] = None  # for synthetic: original clip path
    phash: Optional[str] = None


def load_manifest(path: str | Path) -> list[ClipMeta]:
    """Load a JSONL manifest into a list of ClipMeta objects."""
    path = Path(path)
    clips: list[ClipMeta] = []
    with open(path) as fh:
        for line in fh:
            row = json.loads(line)
            clips.append(ClipMeta(**row))
    log.info("Loaded %d clips from %s", len(clips), path)
    return clips


def save_manifest(clips: list[ClipMeta], path: str | Path) -> None:
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w") as fh:
        for clip in clips:
            fh.write(json.dumps(clip.__dict__) + "\n")
    log.info("Saved manifest with %d entries to %s", len(clips), path)


# ── Video reading ─────────────────────────────────────────────────────────────


def _read_frames_decord(
    path: str,
    num_frames: int,
    fps_target: Optional[float] = None,
    use_gpu: bool = False,
) -> np.ndarray:
    """Return (T, H, W, C) uint8 array sampled uniformly from *path*."""
    ctx = gpu(0) if use_gpu else cpu(0)
    vr = VideoReader(path, ctx=ctx)

    total = len(vr)
    if fps_target is not None:
        native_fps = vr.get_avg_fps()
        stride = max(1, round(native_fps / fps_target))
        indices = list(range(0, total, stride))
    else:
        indices = list(range(total))

    # Uniform temporal sampling to exactly num_frames
    if len(indices) <= num_frames:
        sampled = indices
    else:
        step = len(indices) / num_frames
        sampled = [indices[int(i * step)] for i in range(num_frames)]

    frames = vr.get_batch(sampled).asnumpy()  # (T, H, W, C)
    return frames


def _read_frames_torchvision(path: str, num_frames: int) -> np.ndarray:
    """Fallback reader using torchvision (slower)."""
    import torchvision.io as tvio

    video, _, info = tvio.read_video(path, output_format="TCHW", pts_unit="sec")
    total = video.shape[0]
    indices = [int(i * total / num_frames) for i in range(num_frames)]
    frames = video[indices]  # (T, C, H, W)
    frames = frames.permute(0, 2, 3, 1).numpy()  # → (T, H, W, C)
    return frames


def read_video_frames(
    path: str,
    num_frames: int = 16,
    fps_target: Optional[float] = 8.0,
    use_gpu: bool = False,
) -> np.ndarray:
    """Read *num_frames* frames from a video file."""
    if _HAS_DECORD:
        return _read_frames_decord(path, num_frames, fps_target, use_gpu)
    return _read_frames_torchvision(path, num_frames)


# ── Dataset ───────────────────────────────────────────────────────────────────


class VideoClipDataset(Dataset):
    """Torch Dataset over a JSONL manifest.

    Parameters
    ----------
    manifest_path:
        Path to the JSONL clip manifest.
    split:
        If given, only return clips with ``clip.split == split``.
    synth_ratio:
        Fraction of synthetic clips in each epoch.
        0.0 = real only, 1.0 = synth only, 0.5 = equal mix.
        Applied by resampling, not dropping.
    num_frames:
        Frames to sample per clip.
    fps_target:
        Target fps for temporal sampling.
    frame_size:
        Spatial resize (square).  ``None`` keeps original size.
    transform:
        Optional torchvision transform applied to each frame tensor.
    normalize_mean / normalize_std:
        ImageNet-style normalisation values.
    """

    def __init__(
        self,
        manifest_path: str | Path,
        split: Optional[str] = None,
        synth_ratio: float = 0.0,
        num_frames: int = 16,
        fps_target: float = 8.0,
        frame_size: int = 224,
        transform=None,
        normalize_mean: tuple[float, ...] = (0.485, 0.456, 0.406),
        normalize_std: tuple[float, ...] = (0.229, 0.224, 0.225),
        seed: int = 42,
    ) -> None:
        all_clips = load_manifest(manifest_path)

        if split:
            all_clips = [c for c in all_clips if c.split == split]

        real = [c for c in all_clips if not c.is_synthetic]
        synth = [c for c in all_clips if c.is_synthetic]

        self.clips = self._mix(real, synth, synth_ratio, seed)

        self.num_frames = num_frames
        self.fps_target = fps_target
        self.frame_size = frame_size
        self.transform = transform
        self.mean = torch.tensor(normalize_mean).view(3, 1, 1)
        self.std = torch.tensor(normalize_std).view(3, 1, 1)

        # Build label→idx map from manifest
        labels = sorted({c.label for c in self.clips})
        self.label2idx = {l: i for i, l in enumerate(labels)}
        self.idx2label = {i: l for l, i in self.label2idx.items()}

        log.info(
            "Dataset ready: %d clips, %d classes (synth_ratio=%.2f)",
            len(self.clips),
            len(labels),
            synth_ratio,
        )

    @staticmethod
    def _mix(
        real: list[ClipMeta],
        synth: list[ClipMeta],
        ratio: float,
        seed: int,
    ) -> list[ClipMeta]:
        """Return a balanced list honouring *ratio* fraction of synthetics."""
        rng = random.Random(seed)
        if ratio <= 0.0 or not synth:
            return real
        if ratio >= 1.0 or not real:
            return synth
        # Target total = |real| / (1 - ratio)
        n_total = round(len(real) / (1 - ratio))
        n_synth = n_total - len(real)
        if n_synth > len(synth):
            synth_sample = synth * (n_synth // len(synth) + 1)
        else:
            synth_sample = synth
        synth_sample = rng.sample(synth_sample, min(n_synth, len(synth_sample)))
        mixed = real + synth_sample
        rng.shuffle(mixed)
        return mixed

    def __len__(self) -> int:
        return len(self.clips)

    def __getitem__(self, idx: int) -> dict:
        clip = self.clips[idx]
        frames = read_video_frames(
            clip.path,
            num_frames=self.num_frames,
            fps_target=self.fps_target,
        )  # (T, H, W, C) uint8

        tensor = self._preprocess(frames)  # (C, T, H, W) float

        label = self.label2idx.get(clip.label, clip.label_idx)

        return {
            "pixel_values": tensor,
            "labels": torch.tensor(label, dtype=torch.long),
            "is_synthetic": torch.tensor(clip.is_synthetic, dtype=torch.bool),
            "path": clip.path,
        }

    def _preprocess(self, frames: np.ndarray) -> torch.Tensor:
        """Convert (T, H, W, C) uint8 → (C, T, H, W) float normalised."""
        import torchvision.transforms.functional as TF

        t = torch.from_numpy(frames).float() / 255.0  # (T, H, W, C)
        t = t.permute(0, 3, 1, 2)  # (T, C, H, W)

        if self.frame_size:
            t = torch.stack(
                [TF.resize(f, [self.frame_size, self.frame_size]) for f in t]
            )

        t = t.permute(1, 0, 2, 3)  # (C, T, H, W)
        t = (t - self.mean.unsqueeze(1)) / self.std.unsqueeze(1)

        if self.transform:
            t = self.transform(t)

        return t
